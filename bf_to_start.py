code = "[<+>>>>>>>>++++++++++<<<<<<<-]>+++++[<+++++++++>-]+>>>>>>+[<<+++[>>[-<]<[>]<-]>>[>+>]<[<]>]>[[->>>>+<<<<]>>>+++>-]<[<<<<]<<<<<<<<+[->>>>>>>>>>>>[<+[->>>>+<<<<]>>>>>]<<<<[>>>>>[<<<<+>>>>-]<<<<<-[<<++++++++++>>-]>>>[<<[<+<<+>>>-]<[>+<-]<++<<+>>>>>>-]<<[-]<<-<[->>+<-[>>>]>[[<+>-]>+>>]<<<<<]>[-]>+<<<-[>>+<<-]<]<<<<+>>>>>>>>[-]>[<<<+>>>-]<<++++++++++<[->>+<-[>>>]>[[<+>-]>+>>]<<<<<]>[-]>+>[<<+<+>>>-]<<<<+<+>>[-[-[-[-[-[-[-[-[-<->[-<+<->>]]]]]]]]]]<[+++++[<<<++++++++<++++++++>>>>-]<<<<+<->>>>[>+<<<+++++++++<->>>-]<<<<<[>>+<<-]+<[->-<]>[>>.<<<<[+.[-]]>>-]>[>>.<<-]>[-]>[-]>>>[>>[<<<<<<<<+>>>>>>>>-]<<-]]>>[-]<<<[-]<<<<<<<<]++++++++++."
out = ""
last = "";
count = 1
for c in code:
    if c == last:
        count += 1
    else:
        if count > 1 and last in "<>":
            out += str(count) + last
        elif count > 0 and last in "+-":
            out += "@" + str(count) + "@" + last
        else:
            out += last*count
        count = 1
    last = c

out = out.replace("<", "<;")
out = out.replace(">", ">;")
out = out.replace("-", "-!")
out = out.replace("+", "+!")
out = out.replace("[", ";??[")
out = out.replace("]", ";??]")
out = out.replace(";;", ";")
out = out.replace(".", " PRINT ")
print(out);
print(code);